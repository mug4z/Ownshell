void	ft_zeroinit(int **map, int size)
{
	int	il;
	int	ic;

	il = 0;
	ic = 0;
	while (il < size)
	{
		ic = 0;
		while (ic < size)
		{
			map[ic][il] = 0;
			ic++;
		}
	il++;
	}
}
